 //This program shows how the assignment operators work. 
class Task12 {  
public static void main(String args[])  {  
int    x = 10;   
System.out.println("\nX (Initial value) = "+x); 
x -= 5; 
// x = x - 5;  
System.out.println("X (After x -= 5) = "+ x);  
x *= 5; // x = x * 5;  
System.out.println("X (After x *= 5) = "+ x);  
x /= 5; // x = x / 5;  
System.out.println("X (After x /= 5) = "+ x);  
x %= 5; // x = x % 5;  
System.out.println("X (After x %= 5) = "+ x);  
}
}

